void my_bitmap2(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, uint8_t width, const uint8_t bitmap[]) {
	
 	for (uint8_t y = 0; y < y1-y0; y++) {
		oled.setCursor(x0,y+y0);
		oled.startData();
    uint16_t j = 0;
		for (uint8_t x = x0; x < x1; x++) {
			oled.sendData(pgm_read_byte(&bitmap[y*width+j++]));
		}

		oled.endData();
	}
	oled.setCursor(0, 0);
}

// display repeated times of the bitmap
void my_bitmap3(uint8_t x_off, uint8_t y0, uint8_t y1, uint8_t repeat, const uint8_t bitmap[]) {
	for (uint8_t r = 0; r <= repeat; r++){
		uint8_t x0 = x_off + r;
		for (uint8_t y = y0; y < y1; y++) {
			oled.setCursor(x0,y);
			oled.startData();
			if (x0 == 3 || x0 == 56 || r == repeat)
					oled.sendData(pgm_read_byte(&fill_init[y]));
      else
        oled.sendData(pgm_read_byte(&bitmap[y]));
			oled.endData();
		}
	}
	oled.setCursor(0, 0);
}

void my_bitmap4(uint8_t x_off, uint8_t y0, uint8_t y1, uint8_t repeat, const uint8_t bitmap[]) {
	for (uint8_t r = 0; r <= repeat; r++){
		uint8_t x0 = x_off + r;
		for (uint8_t y = y0; y < y1; y++) {
			oled.setCursor(x0,y);
			oled.startData();
			if (r == 0)
					oled.sendData(pgm_read_byte(&fill_init[y]));
      else
        oled.sendData(pgm_read_byte(&bitmap[y]));
			oled.endData();
		}
	}
	oled.setCursor(0, 0);
}



const uint8_t numFrames = 50;
uint8_t listVoltage[numFrames] = {0};
int count = 0;

int anim_frame = 0; // animation frame for the blob / metaballs animation

int xoff = 3;
int yoff = 0;

void setup() {
  oled.begin(64, 48, sizeof(tiny4koled_init_64x48br), tiny4koled_init_64x48br);
 
  oled.off();

	oled.bitmap(0, 0, 3, 6, battery_indicator_part1);
	my_bitmap3(3, 0, 6, 53, battery_indicator_part2);
	oled.bitmap(56, 0, 64, 6, battery_indicator_part3);
	oled.on();
}

uint8_t pre_x0 = xoff;

int8_t diff_x =0;
uint8_t MAX_x0 = xoff + 1;
uint8_t MAX_x1 = xoff + 53;
uint8_t batteryperc = 0;
int8_t increament = 1;

void loop() {

  delay(1);
	listVoltage[(count/10) % numFrames] = map(sys.getVCC(), 1300, 4000, 0, 100);

	int sum = 0;
	for (int i = 0; i < numFrames; i++){
		sum += listVoltage[i];
	}
	batteryperc = sum / numFrames;
	
  int gauge = map(batteryperc, 0, 100, 0, 53);
	gauge = max(0, min(gauge, 52));

	uint8_t x0 = xoff + gauge;
	diff_x = x0 - pre_x0;

  if(diff_x > 0){
		if(x0 > MAX_x0 && x0 < MAX_x1){
			if (pre_x0+diff_x < MAX_x1)
    		my_bitmap3(pre_x0, 0, 6, diff_x, fill);
			else
				my_bitmap3(pre_x0, 0, 6, MAX_x1-pre_x0, fill);
		}
  }
  else if (diff_x < 0){
		if (pre_x0+diff_x < MAX_x1)
    	my_bitmap4(pre_x0 + diff_x , 0, 6, -diff_x-1, battery_indicator_part2);
    else
      my_bitmap3(pre_x0 + diff_x, 0, 6, MAX_x1-pre_x0-1, fill);

  }

  if (x0 + 32 < 55){
    oled.bitmap(x0+1, 1, x0+1+32, 5, epd_bitmap_allArray[anim_frame]);
		oled.bitmap(x0+1+32, 0, x0+2+32, 6, battery_indicator_part2);
  }else{
    my_bitmap2(x0+1, 1, 56, 5, 32, epd_bitmap_allArray[anim_frame]);
  }
	

  anim_frame = (anim_frame + 1) % (sizeof(epd_bitmap_allArray)/sizeof(epd_bitmap_allArray[0]));
  //oled.on();
  pre_x0 = x0;
	
  count++;
  if (count == 10000){ // to avoid overflow
    count = 0;
  }
}